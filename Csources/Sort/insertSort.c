void insertSort(int *arr, int n)
{
	for(int i=1;i<n;i++)
	{
		int j=i-1;
		int value=arr[i];
		for(;j>=0;j--)
		{
			if(arr[j]>value)
				arr[j+1]=arr[j];
			else
				break;
		}
		arr[j+1]=value;
	}
}


